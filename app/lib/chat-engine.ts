/* ---------- helpers ---------- */
export function getReply(category: EmotionCategory): string {
  const pool = replyPools[category];
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getWhisper(category: EmotionCategory): string {
  const pool = whisperPools[category];
  return pool[Math.floor(Math.random() * pool.length)];
}

/** pick a random reply from an adjacent category for variety */
export function getVariedReply(): string {
  const categories: EmotionCategory[] = [
    "tired", "happy", "anxious", "lost", "bored",
    "late_night", "graduation", "philosophical", "casual"
  ];
  const cat = categories[Math.floor(Math.random() * categories.length)];
  return getReply(cat);
}
