/* ---------- detect emotion from input ---------- */
export function detectEmotion(input: string): EmotionCategory {
  const lower = input.toLowerCase();

  /* check explicit emotions first */
  const categories: EmotionCategory[] = [
    "tired", "anxious", "lost", "happy", "bored",
    "late_night", "graduation", "philosophical"
  ];

  for (const category of categories) {
    for (const keyword of keywordMap[category]) {
      if (lower.includes(keyword)) return category;
    }
  }

  /* heuristics for unclassified input */
  if (lower.length < 6) return "casual";               // very short → casual
  if (lower.includes("?") || lower.includes("？")) return "philosophical"; // questions lean philosophical
  if (lower.length > 60) return "lost";                 // long unstructured text → likely processing feelings

  return "casual";
}

